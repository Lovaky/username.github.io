// 如果需要将数据转换为纯数字格式（小数），可以使用以下函数
function parseValue(value) {
  if (typeof value === 'number') return value;
  if (typeof value === 'string') {
    // 移除"约"和"%"
    const cleaned = value.replace(/[约%]/g, '');
    const num = parseFloat(cleaned);
    // 如果原始字符串包含%，则需要转换为小数
    if (value.includes('%')) {
      return num / 100;
    }
    return num;
  }
  return null;
}

// 示例：获取某学校2024年的数值
function getSchoolValue(schoolName, year) {
  const school = universityData.find(s => s.name === schoolName);
  return school ? parseValue(school[`y${year}`]) : null;
}
